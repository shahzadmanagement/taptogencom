function b07SafetyFor(tool: RolloutToolSpec, language: RolloutLanguage): string {
  const baseSafety = safetyFor(tool, profiles[language]);
  if (tool.kind === 'business' || tool.kind === 'bio') return `${baseSafety} ${b07SafetyAddendum[language].business}`;
  if (tool.kind === 'utility') return `${baseSafety} ${b07SafetyAddendum[language].utility}`;
  if (tool.kind === 'name') return `${baseSafety} ${b07SafetyAddendum[language].name}`;
  if (tool.kind === 'creative' || tool.kind === 'gaming') return `${baseSafety} ${b07SafetyAddendum[language].fictional}`;
  if (tool.kind === 'text') return `${baseSafety} ${b07SafetyAddendum[language].text}`;
  if (tool.kind === 'seo') return `${baseSafety} ${b07SafetyAddendum[language].seo}`;
  if (tool.kind === 'social') return `${baseSafety} ${b07SafetyAddendum[language].social}`;
  if (tool.kind === 'developer') return `${baseSafety} ${b07SafetyAddendum[language].developer}`;
  if (tool.kind === 'writing') return `${baseSafety} ${b07SafetyAddendum[language].writing}`;
  return baseSafety;
}

export const rolloutB07LocalizedToolData: LocalizedToolContent[] = tools.flatMap((tool) =>
  languages.map((language) => {
    const profile = profiles[language];
    const h1 = phrase(profile, tool);
    const primaryKeyword = h1.toLowerCase();
    const safety = b07SafetyFor(tool, language);
    return {
      canonicalToolId: tool.canonicalToolId,
      language,
      primaryKeyword,
      localizedSlug: slugFor(language, tool),
      h1,
      metaTitle: `${h1} ${profile.free}`,
      metaDescription: `${profile.intro} ${safety}`.slice(0, 220),
      intro: profile.intro,
      faqTopics: profile.faqTopics,
      searchIntentNote: profile.intent,
      riskSafetyNote: safety,
    };
  }),
);
